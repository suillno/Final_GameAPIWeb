CREATE TABLE GAME_CART (
    CART_ID          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_NAME        VARCHAR2(50) NOT NULL,
    GAME_ID          NUMBER NOT NULL,
    TITLE            VARCHAR2(200) NOT NULL,
    BACKGROUND_IMAGE VARCHAR2(300),
    PRICE            NUMBER(10,2) NOT NULL,
    SALE_PRICE       NUMBER(10,2),  -- NOT NULL 제외됨
    CREATED_AT       TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    
    CONSTRAINT UK_GAMECART_USER_CART UNIQUE (USER_NAME, CART_ID)
);

COMMIT;