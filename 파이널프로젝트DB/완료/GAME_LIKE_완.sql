CREATE TABLE GAME_LIKE (
    LIKE_ID           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- 자동 증가
    USER_NAME         VARCHAR2(30) NOT NULL,
    GAME_ID           NUMBER NOT NULL,
    TITLE             VARCHAR2(200),
    BACKGROUND_IMAGE  VARCHAR2(300),
    PRICE             NUMBER,
    SALE_PRICE        NUMBER,
    CREATED_AT        TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT UK_GAME_LIKE_USER_GAME UNIQUE (USER_NAME, GAME_ID)
);
